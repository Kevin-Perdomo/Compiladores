/*
Em Java, um enum é uma estrutura de dados especial que representa um conjunto fixo de valores constantes.
Cada elemento deste enum corresponde a um tipo de variável em nossa linguagens de programação

São eles:
BOOL: Este elemento representa o tipo booleano, que pode armazenar valores verdadeiro (true) ou falso (false).
INT: Este elemento representa o tipo inteiro, que pode armazenar números inteiros, como -2, -1, 0, 1, 2, etc.
FLOAT: Este elemento representa o tipo de ponto flutuante, que pode armazenar números decimais, como 3.14, -0.5, etc.
CHAR: Este elemento representa o tipo caractere, que pode armazenar um único caractere, como 'a', 'b', '1', '@', etc.
 */

public enum TipoVariavel{BOOL,INT,FLOAT,CHAR;}